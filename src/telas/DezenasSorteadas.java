    private void jb_sorteadasMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_sorteadasMouseEntered
        ta_dezenas.setText("");
        for (int i = 0; i < BtSelecionados.size(); i++) {
            if (BtSelecionados.get(i)<10) {
                ta_dezenas.setText(ta_dezenas.getText()+"0"+BtSelecionados.get(i)+" ");
            } else {
                ta_dezenas.setText(ta_dezenas.getText()+BtSelecionados.get(i)+" ");
            }
        }
    }//GEN-LAST:event_jb_sorteadasMouseEntered

    private void jb_sorteadasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_sorteadasActionPerformed
        if(BtSelecionados.size() == 15) {

            Volante.loteria.setDezenasSorteadas(BtSelecionados);
            Volante.loteria.imprimeArray(Volante.loteria.getDezenasSorteadas());
            
            JOptionPane.showMessageDialog(null,"Dezenas Sorteadas Cadastradas. Clique em fechar.", "Dezenas Cadastradas!", JOptionPane.INFORMATION_MESSAGE );
            
        }else{
            JOptionPane.showMessageDialog(null,"Escolha 15 dezenas.", "Dezenas não cadastradas!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jb_sorteadasActionPerformed

